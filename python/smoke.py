#! /usr/bin/env python3
import life
import lifeutils

def test() :
	X, Y, start = lifeutils.readt("""
..........
......x...
....xxx...
.....x....
..........
..........
..........
..........
..........
..........
""")

	#print(X, Y, start)
	#print(lifeutils.savet(X, Y, start))
	end = [False] * X * Y
	res = life.run(X, Y, 1, 100, start, end)

	print(lifeutils.savet(X, Y, end), end='')

	assert lifeutils.savet(X, Y, end) == """\
..........
..x.......
.x.x......
.x..x.x...
.xxxxx.x..
.xx..xx...
....x.....
....x.....
..........
..........
"""

	assert res == 100

	print("Passed")


if __name__ == "__main__" :
	test()
